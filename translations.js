// Get current language from localStorage or default to Romanian
function getCurrentLanguage() {
  return localStorage.getItem('site-language') || 'ro';
}

// Set language and save to localStorage
function setLanguage(lang) {
  localStorage.setItem('site-language', lang);
  updatePageLanguage(lang);
}

// Update all text on the page
function updatePageLanguage(lang) {
  const t = translations[lang];
  
  // Navigation
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });
  
  // Language toggle button
  const langToggle = document.getElementById('lang-toggle');
  if (langToggle) {
    langToggle.textContent = lang === 'ro' ? 'EN' : 'RO';
    langToggle.setAttribute('data-current-lang', lang);
  }
  
  // Update page title if it has translation
  const pageTitle = document.querySelector('title');
  if (pageTitle) {
    const titleKey = pageTitle.getAttribute('data-i18n-title');
    if (titleKey && t[titleKey]) {
      pageTitle.textContent = t[titleKey];
    }
  }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  let currentLang = getCurrentLanguage();
  updatePageLanguage(currentLang);
  
  // Setup language toggle button
  const langToggle = document.getElementById('lang-toggle');
  if (langToggle) {
    langToggle.addEventListener('click', function() {
      const newLang = currentLang === 'ro' ? 'en' : 'ro';
      setLanguage(newLang);
      currentLang = newLang;
    });
  }
});
